package chap03.examples;

public class BitLogicExample {
	public static void main(String[] args) { 
		System.out.println("45 & 25 = " + (45 & 25)); // 9 출력
		System.out.println("45 | 25 = " + (45 | 25)); // 61 출력
		System.out.println("45 ^ 25 = " + (45 ^ 25)); // 52 출력
		System.out.println("~45 == " + ~45 ); // -46 출력 
		
	}
}
